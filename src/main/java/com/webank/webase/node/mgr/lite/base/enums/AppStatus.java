package com.webank.webase.node.mgr.lite.base.enums;

/**
 * Enumeration of app status.
 */
public enum AppStatus {
    UNREGISTER(0), NORMAL(1), INVALID(2);

    private int value;

    AppStatus(Integer dataStatus) {
        this.value = dataStatus;
    }

    public int getValue() {
        return this.value;
    }
}
